package com.ysoztf.wechat.analyze.wxinfo.beans;

public class WxBiasInfo {
    private long nameBias;
    private long accountBias;
    private long mobileBias;
    private long mailBias;

    public long getNameBias() {
        return nameBias;
    }

    public void setNameBias(long nameBias) {
        this.nameBias = nameBias;
    }

    public long getAccountBias() {
        return accountBias;
    }

    public void setAccountBias(long accountBias) {
        this.accountBias = accountBias;
    }

    public long getMobileBias() {
        return mobileBias;
    }

    public void setMobileBias(long mobileBias) {
        this.mobileBias = mobileBias;
    }

    public long getMailBias() {
        return mailBias;
    }

    public void setMailBias(long mailBias) {
        this.mailBias = mailBias;
    }
}
